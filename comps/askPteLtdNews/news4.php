орится в отчете McKinsey. Такой динамичный рост, по мнению авторов статьи, повлияет на перераспределение ролей среди участников рынка. Лидерами станут те, которые способны оперативно мобилизовать все свои активы для удовлетворения потребностей заказчиков. При этом они должны быть нацелены на азиатские каналы поставок. Отстающим будет очень сложно наверстать упущенное, они потеряют преимущества переходного периода. Поэтому может произойти поляризация рынка этих услуг, его укрупнение. Ранее Kursiv.kz писал о том, что Союз транспортников Казахстана предложил развивать в Казахстане перспективное логистическое направление – рынок контейнерных перевозок. Сейчас уровень контейнеризации в железнодорожных перевозках РК не превышает 2-3%. А можно довести эту цифру до 11%.
