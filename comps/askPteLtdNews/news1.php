вод в Таиланде), поставщика бытовой электроники Panasonic (закрыл завод в США ради фабрики в Малайзии), производителя обуви и аксессуаров Steven Madden (перенес предприятие из Китая в Камбоджу) и компанию Kayamatics, которая занимается интернетом вещей. Сейчас у этой фирмы два завода в Китае, но в ближайшее время она откроет две производственные линии в Малайзии.Поставщик деталей для Apple, компания Delta Electronics, готова потратить $2,1 млрд на приобретение завода в Таиланде, чтобы наладить там изготовление комплектующих. Фирма Merry Electronics, собирающая наушники по заказу Bose и других известных марок, также намерена перенести часть своего производства из южного Китая в Таиланд.

