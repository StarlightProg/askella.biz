						<p class="main__text"> Производители жженного кирпича в Узбекистане из-за того, что у потребителя нет альтернативы при покупке их продукции, готовы и дальше использовать старое и энергонеэффективное оборудование, что влияет на удорожание конечной продукции и всего процесса строительства, сообщает корреспондент Podrobno.uz со ссылкой на Минэнерго.<br><br>

"К сожалению, сейчас, как показывает практика, абсолютное большинство теплиц и предприятий по производству керамики и жженого кирпича используют достаточно допотопные технологии, они крайне энергонеэффективны. Жженый кирпич по своей специфике – это тот продукт, который очень сложно завезти импортом. Получается, что наши местные производители готовы пережигать газ и не переходить на новое энергосберегающее оборудование, так как для этого нет стимулов", – рассказал в ходе пресс-конференции в Ташкенте заместитель министра энергетики Шерзод Ходжаев.<br><br>

По его словам, пока будут сохраняться такие безальтернативные условия, все это будет отображаться на строительстве, и в конечном итоге от этого страдает народ.<br><br>

Что касается соответствия экологическим нормам, то, по словам Ходжаева, это вполне решаемый вопрос.
"На всех электростанциях, в том числе тех, которые находятся на территории Узбекистана, используется технология очистки выбросов. Это так называемые электрофильтры. Эта недорогая и достаточно эффективная технология. В Европе нормы экологии гораздо более жесткие, чем у нас, но там есть определенные страны, в топливном балансе которых уголь занимает львиную долю. Использование подобного рода электрофильтров в наших реалиях позволит снизить выбросы в атмосферу до минимума", – подчеркнул он.<br><br>

Ходжаев также добавил, что использование электрофильтров приведет к тому, что производители будут стремиться устанавливать у себя и достаточно энергоэффективные котлы, что будет означать более полное сгорание угля и положительно влиять на выбросы и экологию.<br><br>

"Говоря простым языком – хороший угольный котел и горелка гораздо более экономичны и экологичны, чем плохое газовое оборудование", – отметил он.<br><br>

По словам представителя "Нефтегазинспекции", также высказавшегося на пресс-конференции, действительно в мире есть тенденция по снижению использования угля в качестве топлива. Однако здесь есть свой нюанс.
"Важно понимать, что эта тенденция характерна для стран, где уголь, в качестве топлива, используется на уровне 50% в общем энергобалансе. В Узбекистане этот показатель составляет не более 5%, поэтому на экологии переход на уголь болезненно не отразится. У нас сейчас нет такого развития альтернативных источников энергии, поэтому мы вынуждены переходить на использование угля, так как он на 50% дешевле нежели природный газ", – сказал он.

