													Сначала сахар-сырец смешивают с сахарным сиропом, чтобы растворить остатки мелассы, обволакивающей кристаллы. Полученную смесь (аффинационный утфель) центрифугируют. Отцентрифугированные кристаллы промывают паром, получая почти белый продукт. Его растворяют, превращая в густой сироп, добавляют туда известь и фосфорную кислоту, чтобы примеси всплыли в виде хлопьев, а затем фильтруют через костяной уголь (черный гранулированный материал, получаемый из костей животных). Главная задача на этом этапе – полное обесцвечивание и обеззоливание продукта. На рафинирование 45 кг растворенного сахара-сырца расходуется от 4,5 до 27 кг костяного угля. Точное соотношение не устанавливается, поскольку поглощательная способность фильтра по мере его использования снижается. Полученную белую массу выпаривают и после кристаллизации центрифугируют, т.е. поступают с ней примерно так же, как с соком сахарного тростника, после чего рафинированный сахар сушат, удаляя из него остатки (ок. 1%) воды.
