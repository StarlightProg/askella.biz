													Сахарная свекла – объемистый и скоропортящийся продукт, поэтому заводы по ее переработке обычно строятся поблизости от плантаций. Для получения 45 кг сахара из примерно 290 кг свеклы требуется ок. 27 кг угля и 16 кг извести и кокса. Процесс слагается из уже описанных стадий: экстракции, очистки, выпаривания и кристаллизации.<br>
Сначала свеклу моют, а потом нарезают в стружку, которую загружают в диффузор, где сахар экстрагируется из растительной массы горячей водой. В результате получают «диффузионный сок», содержащий от 10 до 15% сахарозы. Остающийся свекловичный жом служит прекрасным кормом для скота. <br><br>

Диффузионный сок смешивают в сатураторе с известковым молоком. Здесь оседают тяжелые примеси. Затем через нагретый раствор пропускают диоксид углерода, чтобы известь связала несахара. Отфильтровав их, получают т.н. «очищенный сок». Отбеливание включает пропускание через него сернистого газа, а затем фильтрование через активированный уголь. Избыток воды удаляют выпариванием. Полученная в конечном итоге жидкость содержит от 50 до 65% сахара.

