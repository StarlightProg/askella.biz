						<p class="main__title">В Казахстане решилась проблема дефицита вагонных запчастей</p>
						<p class="main__text">Коллегия Евразийской экономической комиссии по предложению Республики Казахстан дополнила перечень моделей железнодорожных локомотивов и вагонов, по которым допускается выпуск в обращение без документов об оценке соответствия составных частей подвижного состава. Речь идет о запчастях для обслуживания и ремонта железнодорожного транспорта, введенного в эксплуатацию до 2 августа 2014 года.Отметим, что к железнодорожному транспорту в Евразийском экономическом союзе предъявляются повышенные требования прежде всего в целях безопасности граждан союзных стран. Проверка качества и безопасности таких товаров является одной из самых строгих, а доказательством соответствия продукции установленным стандартам является сертификат соответствия. Этот документ обязателен для производителей, импортеров и продавцов.Оценка соответствия проводится на основании трех действующих технических регламентов, которые приняты странами Союза: о безопасности железнодорожного подвижного состава, высокоскоростного железнодорожного транспорта и инфраструктуры железнодорожного транспорта.Но есть исключения из правила, которые принимаются, например, в связи с определенными экономическими особенностями в том или ином государстве ЕАЭС. Соответствующим решением Комиссии вышеназванный перечень дополнен моделями полувагонов 12-9920 и 12-9941, которые произведены в Казахстане. При их ремонте и обслуживании можно использовать без документов об оценке соответствия роликовые подшипники качения для конических букс. Однако эксплуатировать такие полувагоны допускается только в Казахстане, где будет установлен строгий контроль национальных уполномоченных органов.Это решение ЕЭК позволит избежать дефицита подшипников, необходимых для ремонта названных полувагонов, и обеспечит бесперебойную работу вагонного парка страны в условиях нестабильности поставок запчастей.</p>


