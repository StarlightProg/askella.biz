						<p class="main__text">Модные цвета осень-зима 2021 − 2022 от института цвета Pantone Color Institute, которые были представлены для палитры модных цветов осенне-зимней коллекции 2021-2022 гг.  <br><br>

						Каждый сезон Pantone Color Institute разрабатывает собственный отчет о цветовых тенденциях будущего сезона. В палитру Pantone Fashion Color Trend Report NY входят ключевые цвета использованные в модных коллекциях представленных на NY Fashion Week.<br><br>
 Mykonos Blue – это ярко-синий цвет, глубокий и насыщенный, будто южное море. Возможно, кому-то он напомнит теплое Эгейское море, хотя цвет моря бесконечно разнообразен – от бирюзового до лазурного, от голубого до темно- синего.<br><br>
 Illuminating – главный цвет 2021 года. Солнечный, радостный, как летнее солнце, он озаряет всю осенне-зимнюю коллекцию, дарит тепло и надежду.  Лепреконы – что это или…кто? Это крошечные лесные человечки из ирландских сказок. Лепрекон – персонаж ирландского фольклора, маленький волшебник, исполняющий желания. Его часто изображают одетым во всё зелёное. В сезоне 2021 – 2022 Pantone включил в модную палитру цвет зеленых трав Ирландии.<br><br>

Fuchsia Fedora- как красив этот яркий цвет! Наверное, не сразу задумаешься – подходит тебе он или нет. Скорее всего, представляешь наряд, который непременно украсит. Почему так? Возможно потому, что его ярко-розовый с сиреневым оттенком цвет уже издалека заставляет замечать себя, а может его интенсивная энергетика привлекает внимание к себе? В любом случае этот цвет символизирует радость, романтизм и счастье, он придает одежде праздничность и игривость, а еще он говорит о том, что его обладательница озаряет все вокруг себя и поделится со всеми своим хорошим настроением.<br><br>
 Pale Rosette- первое, что напомнит нежный розовый цвет– прекрасные розы – трепетные и романтичные, которые олицетворяют юность и непорочность, зарождающиеся романтические чувства. А в одежде такой цвет – символ изысканности и элегантности.<br><br>
 Adobe- тёплый и такой земной цвет. Этот терракотовый оттенок керамики означает в переводе с итальянского – «обожженная земля» (terrа cotta). Правда и керамика имеет разные оттенки, но тем не менее, в терракотовом цвете ощущается комфорт и стабильность.<br><br>
 Fire Whirl – это огненный вихрь. Яркий оттенок красного заряжает все вокруг себя своей «огненной энергией».<br><br>
 Родонит в модной палитре 2021 – 2022 года – это темно-фиолетовый цвет с синим оттенком. Цвет глубокий и таинственный. Однако, само слово – родонит ассоциируется совсем с другим цветом, а именно, с розовым, то есть с цветом уральского камня родонита. Но Институт цвета Pantone имеет свои цветовые предпочтения, а также их названия.<br><br>
 Темно-фиолетовый цвет – благородный и действительно загадочный, поэтому в новом сезоне непременно воспользуйтесь нарядами в этом цвете.<br><br>
 Spring Lake – «Весеннее озеро». Это спокойный светло-голубой оттенок, глядя на который в воображении всплывают родные голубыеозера…<br><br>
 Root Beer- казалось бы, этот цвет может иметь лишь одно название – коричневый, возможно еще – шоколадный. Но Институт цвета обладает уникальной фантазией, предлагая названия разным цветовым оттенкам. И в данном случае – это «корневое пиво», или рутбир. Этот напиток действительно изготавливается из коры дерева, которое называется сассафрас, и является популярным в Северной Америке.<br><br>
 Coconut Cream – «Кокосовый крем». Нежный белый цвет с едва заметным розовым оттенком.<br><br>
 Ultimate Grey - «Абсолютно серый» - второй главный цвет нынешнего 2021 года. Идеально-нейтральный. Спокойный. Уверенный. Надёжный. Да ещё и универсальный – сочетается буквально со всеми цветами и оттенками.<br><br>
 Olive Branch – «Оливковая ветвь». Коричнево-зелёный оттенок, напоминающий оливки. Стилисты считают, что оливковый цвет внесезонный, апотому подойдет и в летний, и в осенний сезон. Летом он красиво сочетается с белым и золотисто- желтым, а осенью его можно комбинировать с бежевым, черным, серым, желтым. Сам цвет вносит в осеннюю палитру мягкость и тепло.  Soybean – Соя. Светлый бежевый оттенок, который легко принимает в своем соседстве и другие цвета.
