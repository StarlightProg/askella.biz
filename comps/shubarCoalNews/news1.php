На сегодняшний день предприятие реализует уголь коммунально-бытового назначения бесперебойно.  При этом реализация угля для коммунально-бытовых нужд происходит посредством биржевых торгов. Соответственно, формирование цены происходит по итогам торгов на бирже. То есть рынок сам определяет цену реализации. Определенные по результатам биржевых торгов оптовые покупатели угля самостоятельно формируют территорию собственных продаж, исходя из спроса потребителей. <br><br>

АО «Шубарколь комир» не продает уголь напрямую населению. Как стало известно, АО «Шубарколь комир» в первую очередь направляет добываемый уголь на внутренний рынок. На экспорт реализуется уголь тех фракций, которые используются энергетическими предприятиями, не домохозяйствами. <br><br>

Сегодня АО «Шубарколь комир» является крупнейшим производителем высококачественного низкозольного угля в Казахстане. Эта компания осуществляет добычу угля открытым способом. Как известно, шубаркольский уголь отличается низкой зольностью (всего 4%) и высокой калорийностью (до 6000 ккал/кг). Благодаря этим уникальным характеристикам шубаркольский уголь является высокоэффективным источником энергии для отопления жилых домов и по праву считается экологически чистым энергоносителем. ERG поставляет уголь в более чем 20 стран в Европе, Азии и других частях света. <br><br>

Эта новость не смогла оставить равнодушными людей, проживающих в населенных пунктах. Если ранее цена за тонну угля составляла 11500 тенге, то с начала января она возросла в разы. В целом ситуация по Карагандинской области остается нестабильной. В настоящее время общая погрузка угля составляет более 61 тысячи вагонов, то есть 4, 202 тысячи тонн в сравнении с аналогичным периодом погрузка снижена на 1,8% или на 375 вагонов (78 тысяч тонн). Причина невыполнения плана - отказы грузоотправителей, а также неприбытие порожних вагонов. Недогрузки вагонов с углем наблюдаются на станциях Нура, Караганда-Угольная, Кызылжар. <br><br>

