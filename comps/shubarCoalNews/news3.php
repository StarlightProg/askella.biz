Чтобы не допустить дефицит угля в близлежащих к населённых пунктах и конфликтов, в этом году открывать пункт самовывоза угля на территории АО “Шубарколь комир” не будут, сообщили в компании. <br>
“Ежегодно в период высокого спроса на уголь на площадке пункта самовывоза наблюдается рекордное скопление автотранспорта. В день здесь собиралось до 400 автомобилей, в среднем водитель простаивал в очереди трое суток. Также в 2022 году непродуктивные посредники экспортировали существенные объёмы каменного угля, предназначенного для коммунально-бытовых нужд, в том числе населения. Этот уголь они приобретали с пункта самовывоза. Такая ситуация вызвала дефицит угля на внутреннем рынке”, – рассказали в АО “Шубарколь комир”.<br>
Теперь уголь будут продавать на топливных базах следующих населённых пунктах: <br>
Улытауская область – город Жезказган и посёлок Жана-арка;<br>
Карагандинская область – посёлок Осакаровка;<br>
Костанайская область – город Аркалык, станция Аманкарагай.<br>
“Для жителей посёлков Нуринского района Карагандинской области, которые находятся в отдалённости от железнодорожных станций (Баршино, Бестамак, Жанбобек, Каракаска, Куланотпес, Талдысай, Ткенекты, Соналы и Ныгман), откроют дополнительные топливные базы на максимально близком расстоянии к их населённым пунктам”, – отметили в компании. 
Столичные жители могут купить уголь в посёлке Коргалжын в трёх розничных точках продажи.<br>
