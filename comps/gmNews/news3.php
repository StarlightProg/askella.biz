						<p class="main__title">Спотовые ставки на контейнерные перевозки продолжают снижаться</p>
						<p class="main__text">Казахстан увеличил поставки своего угля в страны Евросоюза в два раза
Перед вступлением в силу эмбарго на российский уголь европейские страны активно ищут альтернативных поставщиков топлива на замену. Ситуацией начал успешно пользоваться Казахстан — уже в январе-мае он поставил в ЕС почти в два раза больше угля, чем за весь 2021 год, пишет «Коммерсантъ».<br><br>

По данным Eurostat, после ввода российских войск на Украину казахстанские поставщики отправляют в Европу по 300-400 тысяч тонн угля, в то время как год назад в апреле и мае экспорт в этом направлении составлял только 60-70 тысяч тонн.<br><br>

В мае европейские компании резко увеличили закупки и российского угля, но в этом случае речь шла о попытке накопить топливо до запрета, вступающего в силу в августе. Так что в июне интерес к топливу из России ожидаемо сократился.<br><br>

Эксперты полагают, что Казахстан может продолжить наращивать продажу угля в страны Евросоюза, если только не столкнется с противодействием России. Топливо идет в Европу с перевалкой в портах России или странах Балтии, при этом последний маршрут тоже подразумевает использование российской сети железных дорог.<br><br>

В целом для Европы замена угля из России станет проблемой, но решаемой. Увеличить поставки могут Индонезия, Австралия, ЮАР, Колумбия, Танзания и Нигерия. Если Казахстан столкнется с трудностями при транзите через Россию, он может перенаправить его через порт Актау на Каспийском море, чтобы затем через Азербайджан и Грузию доставить его в Черное море.<br><br>

Ранее казахстанские предприниматели жаловались на то, что Россия квотирует поставки их угля на Украину. Кроме того, в 2022 году Россия начала создавать препятствия для прокачки казахстанской нефти. На этом фоне глава республики Касым-Жомарт Токаев поручил наладить продажи нефти в обход России с использованием западных инвесторов.

