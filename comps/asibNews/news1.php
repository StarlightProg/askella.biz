						<p class="main__title">Железнодорожные тарифы на перевозку грузов в 2023 году вырастут на 8%
</p>
						<p class="main__text">Железнодорожные тарифы на перевозку грузов с 1 января 2023 года вырастут на 8%, говорится в пояснительной записке к проекту приказа Федеральной антимонопольной службы (ФАС) РФ, размещенном на официальном сайте правовой информации.<br><br>

"<...> индексация тарифов на перевозки грузов и оказания услуг по использованию инфраструктуры железнодорожного транспорта общего пользования при перевозках грузов в 2023 году составит 8%, в 2024 году - 7,1%, в 2025 году - 4,6%", - отмечается в документе.<br><br>

В пресс-службе ФАС добавили, что тарифные решения направлены на устойчивость работы холдинга "Российские железные дороги" (РЖД) и в производственной, и в социальной сфере. "Данное решение также поможет обеспечить приобретение локомотивов у российских предприятий транспортного машиностроения. В совокупности все это позволит сформировать заказ для смежных отраслей российской экономики, что, в свою очередь, гарантирует сохранение рабочих мест в этих отраслях", - заключили в министерстве.<br>
Предлагаемая индексация железнодорожного тарифа на перевозку грузов позволит сбалансировать финансовый план РЖД и обеспечить исполнение инвестиционной программы холдинга, пояснили в Минтрансе РФ. Также за счет индексации будут направлены средства для развития Восточного полигона.<br><br>

Погрузка на сети РЖД по итогам января-октября 2022 года, по оперативным данным, составила 1,03 млрд тонн, что на 3,6% меньше, чем за аналогичный период прошлого года.<br><br>

В октябре 2022 года погрузка составила 107 млн тонн, что на 3% меньше, чем за аналогичный период прошлого года. Темпы спада замедлились по сравнению с -5,3% в сентябре.<br><br>

При этом за январь-октябрь текущего года компания «РЖД» увеличила свои доходы от основной деятельности по сравнению с прошлым годом более чем на 13%. Они составили 1 трлн 842 млрд руб., сообщил rzd-partnet.ru со ссылкой на свои источники, близкие к холдингу.<br>
Доходы от пассажирских перевозок составили 2,7 млрд руб. (+3,2% к плану). Доходы от грузовых перевозок составили 163 млрд руб. (+1,8% к плану). В Центральной дирекции инфраструктуры ОАО «РЖД» (ЦДИ) за 10 месяцев расходы по перевозочным видам деятельности составили 99,9% к плану (417 млрд руб.), себестоимость перевозок по ЦДИ – 97,9% к плану.<br>
Инвестиционная программа РЖД по части ЦДИ выполнена на 104,6% к плану.

</p>

