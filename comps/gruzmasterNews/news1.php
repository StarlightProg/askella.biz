«Liberty» в переводе с английского означает «свобода», что полностью отражает главный принцип коллекции – «свободу формы». Основополагающим «стержнем» коллекции Liberty является наличие в составе тканей спандекса, что позволяет изделиям из них выглядеть элегантно, держать форму и не деформироваться в процессе эксплуатации.  <br><br>
На данный момент в состав коллекции LIBERTY входят ткани: Liberty-люкс, Liberty- форма, Liberty-стрейч, Liberty-оптима и Liberty-ресто.<br>
Разные ткани коллекции LIBERTY обладают различными преимуществами перед другими тканями (в зависимости от сферы применения) :<br>
• Содержание спандекса – идеальный силуэт одежды, идеальная посадка. Изделия долго сохраняют первоначальную форму;<br>
