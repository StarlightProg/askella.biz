<br><br>
Но ведь с комфортом можно не только заниматься спортом или покорять Эверест. С комфортом можно отдыхать (и вот функциональные ткани захватывают сегмент Outdoor) , а можно и работать с комфортом.
PROXIMA – это плотные дышащие эластичные (в четырех направлениях) ткани с высокой стойкостью к истиранию и усилиям на разрыв, которые прекрасно подходят для пошива, как элитной спецодежды, так и городской повседневной одежды.
<br><br>
Ткани PROXIMA имеют двойное переплетение. Благодаря петельчатой структуре изнаночной стороны, ткани PROXIMA обладают более низкой теплопроводностью по сравнению с обычными тканями. Полиамидное соединение нейлон 6 в составе придает материалам повышенную прочность (ткани PROXIMA не цепляются и не рвутся), а большой процент спандекса позволяет изделиям из них хорошо растягиваться и возвращаться к первоначальной форме, не затрудняя движений и обеспечивая красивый внешний вид. Все ткани PROXIMA в обязательном порядке имеют пропитку DWR (Durable Water Repellent), благодаря которой их невозможно намочить.

</p>
<img style="width: 100%" src="../../img/comps/npkMir/1.png">
<p class="main__text">VORTEX – это не только многослойные мембраны, но и современные термоклеевые материалы, позволяющие скреплять между собой ткани без использования ниток и привычных нам игольных швейных машин, способствуя экономии времени на сборке изделия и обеспечивая создание инновационной, модной и прочной одежды.<br>
Компания OLDOS предлагает со склада высокотехнологичные термоклеевые материалы: ленты VORTEX-mesh (цветную и под цвет сетки трехслойных мембран) и VORTEX-elastic mesh для проклейки швов, дублирующие пленки VORTEX-glue, декоративные пленки с объемным с PVC-нанесением VORTEX-decorate для декорирования и усиления проблемных зон в одежде, термоклеевые пленки VORTEX-color для декорирования изделий, световозвращающие термоклеевые ленты и пленки VORTEX-reflective, с помощью которых получается создавать сложнейшие конфигурации световозвращающих элементов на спецодежде, а также водонепроницаемые молнии и окантовочную резинку.
</p>
