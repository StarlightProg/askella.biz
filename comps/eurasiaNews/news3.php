							В Министерстве сельского хозяйства состоялась встреча с представителями Ассоциации общественного питания, в ходе которой обсуждались вопросы обеспечения бизнеса необходимыми объемами сахара.<br><br>

По итогам обсуждений было принято решение дополнительно поддержать малый и средний бизнес и покрыть потребности объектов общепита в индустриальном сахаре.  <br><br>
Сообщается, что в течение сегодняшнего дня, 28 июля, будут определены объемы потребности сахара для объектов общепита с целью организации поставок в приоритетном порядке.<br>
На первом этапе более тысячи тонн сахара будет отгружено в три наиболее населенных региона.<br>
"Министерством сельского хозяйства будет направлен дополнительный объем в Нур-Султан, Алматы и Шымкент, где представители малого бизнеса смогут брать необходимый объем сахара. Также ассоциацией будут представлены списки и расчеты потребности по другим городам. При этом ассоциация и акиматы разработают алгоритм по предоставлению данного сахара", - заявил на совещании вице-министр сельского хозяйства Женис Осербай.<br>
Во встрече, кроме вице-министра, также приняли участие председатель Комитета торговли МТИ Ержан Казанбаев, советник председателя Ассоциации общественного питания Вероника Нурпеисова и представители акимата Нур-Султана.


