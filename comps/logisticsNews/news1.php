						<p class="main__title">В России могут вырасти тарифы на контейнерные перевозки на восточном направлении</p>
						<p class="main__text">Министр транспорта России Виталий Савельев в ходе совещания с президентом Владимиром Путиным предложил ряд мер, направленных на стабилизацию ситуации с перевозками грузов на восточном направлении.
«Первая мера — это перевозки с использованием автомобильного транспорта. В общем объёме перевозок импортных контейнеров с Дальнего Востока автомобильная перевозка достигла 9% от общего объёма. По сути, это тот максимум, на который можно использовать автомобильный транспорт на таком большом, в общем-то, плече», — заявил Виталий Савельев.

Второй мерой, по его словам, могло бы стать увеличение количества контейнерных поездов с полувагонами из терминалов морских портов Дальнего Востока с одного до пяти в сутки. Это позволит вывозить до 710 контейнеров ДФЭ в сутки или до 21 тысячи ДФЭ в месяц.

«Третья мера — увеличить количество контейнерных поездов в восточном направлении на три пары в сутки сроком до конца текущего года, то есть фактически на полтора месяца, за счёт снижения перевозок товаров и, в том числе, угля. Данное решение, кроме импорта, позволит увеличить также вывоз экспортных грузов. При этом ожидаемый выпадающий объём отгрузок энергетического угля составит порядка 800 тысяч тонн до конца года или 1,4 процента от годового объёма, который мы планируем довезти, то есть добрать в следующем году — до 15 апреля мы постараемся полностью обеспечить вывоз этого объёма», — уточнил глава Минтранса.

В качестве четвёртой меры он предложил рассмотреть возможность увеличения тарифов на контейнерные перевозки на восточном направлении с одновременным снижением тарифов перевозок в порты Северо-Запада в 2023 году. «Так, при увеличении тарифа условно на десять процентов – удорожание товара не более чем на один процент, но тем самым мы можем стимулировать спрос на вывоз контейнеров на Северо-Запад. Это позволит повысить конкурентоспособность всего маршрута и увеличить объёмы перевозок через порты Северо-Запада», — констатировал Виталий Савельев, попросив главу государства поддержать озвученные предложения.


