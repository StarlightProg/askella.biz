Композиционный или композитный материал, сокращенно композит, – материал, изготовленный из двух или более компонентов с разными свойствами. В результате получается новый материал с новыми характеристиками. В составе композита выделяют матрицу (полимер) и наполнитель (углеродные или стеклянные волокна), который выполняют функцию армирования (по аналогии с арматурой в железобетоне). Сочетание разных компонентов позволяет улучшить характеристики материала и делает его одновременно легким и прочным. Самый простой композит – клееная фанера. Она состоит из слоев шпона, склеенных карбамидной или фенольной смолой.

